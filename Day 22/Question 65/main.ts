// This function Determine the Reminder value
function determineTheReminder(num1: number, num2: number): number{
// We can use % sign to check the Reminder
    return num1 % num2
};

console.log(determineTheReminder(5 , 2)); // The output is "1"
// when we try it with 5 Divided by 2. The leftover value is "1"
